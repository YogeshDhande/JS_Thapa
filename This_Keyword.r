This Keywors:-
    -In an object method, this referr to the object
    -Alone, this refers to the global object.
    -In a function, this refer to the global object.
    -In a function, in strict mode, this is undefined.
    -In a an event, this refer to the element that recevied the event.
    -Method like call(), apply() and bind() can refer this to any object.

